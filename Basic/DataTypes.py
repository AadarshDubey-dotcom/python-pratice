#Data Types 
#In Python, there are several built-in data types that are used to classify the type of value that a variable can hold. Some of the most common data types include:
#1. Integer (int): Represents whole numbers without a decimal point.
#2. Float (float): Represents real numbers with a decimal point.
#3. String (str): Represents a sequence of characters.
#4. Boolean (bool): Represents either True or False.
#5. List: Represents an ordered collection of items.
#6. Tuple: Represents an ordered, immutable collection of items.
#7. Dictionary (dict): Represents a collection of key-value pairs.
#8. Set: Represents an unordered collection of unique items.

